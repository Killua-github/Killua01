Month <- function(mm)
{
  month <- mm
  if(month == 01)
  {
    "Jan"
  }
  else if(month == 02)
  {
    "Feb"
  }
  else if(month == 03)
  {
    "Mar"
  }
  else if(month == 04)
  {
    "Apr"
  }
  else if(month == 05)
  {
    "May"
  }
  else if(month == 06)
  {
    "Jun"
  }
  else if(month == 07)
  {
    "Jul"
  }
  else if(month == 08)
  {
    "Aug"
  }
  else if(month == 09)
  {
    "Sep"
  }
  else if(month == 10)
  {
    "Oct"
  }
  else if(month == 11)
  {
    "Nov"
  }
  else if(month == 12)
  {
    "Dec"
  }
}
